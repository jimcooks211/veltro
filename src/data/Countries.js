/**
 * Derives a flag emoji from a 2-letter ISO country code.
 * Works by converting each letter to a Unicode Regional Indicator Symbol.
 */
export const toFlag = (iso) =>
  [...iso.toUpperCase()].map(c => String.fromCodePoint(0x1F1E6 + c.charCodeAt(0) - 65)).join('')


